#include<iostream>

using namespace std;

int main() {
    int number;

    cin >> number;

    cout << number << " % 2 = " << number % 2 << endl;
    cout << number << " % 3 = " << number % 3 << endl;
    cout << number << " % 5 = " << number % 5 << endl;
    cout << number << " % 8 = " << number % 8 << endl;
    return 0;
}
