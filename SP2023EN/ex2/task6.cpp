#include<iostream>
using namespace std;

int main() {
    int number;
    cin >> number;

    cout << "The remainder when the number " << number << " is divided with 2 is: " << number % 2 << endl;
    cout << "The remainder when the number " << number << " is divided with 3 is: " << number % 3 << endl;
    cout << "The remainder when the number " << number << " is divided with 5 is: " << number % 5 << endl;
    cout << "The remainder when the number " << number << " is divided with 8 is: " << number % 8 << endl;



    return 0;
}
